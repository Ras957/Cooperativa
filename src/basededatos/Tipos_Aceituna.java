    public String getNombreAceituna() {
        return nombreAceituna;
    }

    /**
     * @param nombreAceituna the nombreAceituna to set
     */
    public void setNombreAceituna(String nombreAceituna) {
        this.nombreAceituna = nombreAceituna;
    }
    
    
