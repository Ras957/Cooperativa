public class Trabajadores {
    private int codigoTrabajador;
    private String nombreTrabajador;
    private String apellidosTrabajador;
    private String direccionTrabajador;
    private String emailTrabajador;
    private int telefonoTrabajador;
    private float sueldoTrabajador;
    private String cargoTrabajador;

    /**
     * @return the codigoTrabajador
     */
    public int getCodigoTrabajador() {
        return codigoTrabajador;
    }

    /**
     * @param codigoTrabajador the codigoTrabajador to set
     */
    public void setCodigoTrabajador(int codigoTrabajador) {
        this.codigoTrabajador = codigoTrabajador;
    }

    /**
     * @return the nombreTrabajador
     */
    public String getNombreTrabajador() {
        return nombreTrabajador;
    }

    /**
     * @param nombreTrabajador the nombreTrabajador to set
     */
    public void setNombreTrabajador(String nombreTrabajador) {
        this.nombreTrabajador = nombreTrabajador;
    }

    /**
     * @return the apellidosTrabajador
     */
    public String getApellidosTrabajador() {
        return apellidosTrabajador;
    }

    /**
     * @param apellidosTrabajador the apellidosTrabajador to set
     */
    public void setApellidosTrabajador(String apellidosTrabajador) {
        this.apellidosTrabajador = apellidosTrabajador;
    }

    /**
     * @return the direccionTrabajador
     */
    public String getDireccionTrabajador() {
        return direccionTrabajador;
    }

    /**
     * @param direccionTrabajador the direccionTrabajador to set
     */
    public void setDireccionTrabajador(String direccionTrabajador) {
        this.direccionTrabajador = direccionTrabajador;
    }

    /**
     * @return the emailTrabajador
     */
    public String getEmailTrabajador() {
        return emailTrabajador;
    }

    /**
     * @param emailTrabajador the emailTrabajador to set
     */
    public void setEmailTrabajador(String emailTrabajador) {
        this.emailTrabajador = emailTrabajador;
    }

    /**
     * @return the telefonoTrabajador
     */
    public int getTelefonoTrabajador() {
        return telefonoTrabajador;
    }

    /**
     * @param telefonoTrabajador the telefonoTrabajador to set
     */
    public void setTelefonoTrabajador(int telefonoTrabajador) {
        this.telefonoTrabajador = telefonoTrabajador;
    }

    /**
     * @return the sueldoTrabajador
     */
    public float getSueldoTrabajador() {
        return sueldoTrabajador;
    }

    /**
     * @param sueldoTrabajador the sueldoTrabajador to set
     */
    public void setSueldoTrabajador(float sueldoTrabajador) {
        this.sueldoTrabajador = sueldoTrabajador;
    }

    /**
     * @return the cargoTrabajador
     */
    public String getCargoTrabajador() {
        return cargoTrabajador;
    }

    /**
     * @param cargoTrabajador the cargoTrabajador to set
     */
    public void setCargoTrabajador(String cargoTrabajador) {
        this.cargoTrabajador = cargoTrabajador;
    }
    
}
